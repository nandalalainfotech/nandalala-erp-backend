    async create(assetDTO: AssetDTO): Promise<Asset001mb> {
        const asset001mb = new Asset001mb();
        asset001mb.setProperties(assetDTO);
        return this.assetRepository.save(asset001mb);
    }

    async update(prodorderDTO: AssetDTO): Promise<Asset001mb> {
        const asset001mb = new Asset001mb();
        asset001mb.setProperties(prodorderDTO);
        await this.assetRepository.update({ id: asset001mb.id }, asset001mb);
        return asset001mb;
    }

    async findAll(): Promise<Asset001mb[]> {
        return this.assetRepository.find();
    }

    findOne(id: string): Promise<Asset001mb> {
        return this.assetRepository.findOne(id);
    }

    async remove(id: string): Promise<void> {
        await this.assetRepository.delete(id);
    }

    async downloadPdf(@Req() request: Request, @Res() response: Response) {
        let asset: any[] = await this.assetRepository.find();


        var document = {
            html: html,
            data: {
                Asset: asset,
            },
            path: path.join(`./pdf/asset.pdf`),
            type: "",
        };
        var options = {
            format: "A3",
            orientation: "landscape",
            border: "10mm",
            header: {
                height: "45mm",
            },
            footer: {
                height: "28mm",

            }
        };
        if (asset.length < 0) {
            return;
        }
        else {
            pdf.create(document, options).then((pathRes) => {
                const filestream = createReadStream(pathRes.filename);
                response.writeHead(200, {
                    "Content-Disposition": "attachment;filename=" + "asset.pdf",
                    'Content-Type': 'application/pdf'
                });
                filestream.pipe(response);
            }).catch((error) => {
                console.error(error);
            });
        };

    }

