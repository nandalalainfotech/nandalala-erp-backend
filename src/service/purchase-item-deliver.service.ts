	async downloadPdf(@Req() request: Request, @Res() response: Response) {
        let purchaseItemDeliver: any[] = await this.purchaseItemDeliverRepository.find();


        var document = {
            html: html,
            data: {
                PurchaseItemDeliver: purchaseItemDeliver,
            },
            path: path.join(`./pdf/purchaseItemDeliver.pdf`),
            type: "",
        };
        var options = {
            format: "A3",
            orientation: "landscape",
            border: "10mm",
            header: {
                height: "45mm",
            },
            footer: {
                height: "28mm",

            }
        };
        if (purchaseItemDeliver.length < 0) {
            return;
        }
        else {
            pdf.create(document, options).then((pathRes) => {
                const filestream = createReadStream(pathRes.filename);
                response.writeHead(200, {
                    "Content-Disposition": "attachment;filename=" + "purchaseItemDeliver.pdf",
                    'Content-Type': 'application/pdf'
                });
                filestream.pipe(response);
            }).catch((error) => {
                console.error(error);
            });
        };

    }

