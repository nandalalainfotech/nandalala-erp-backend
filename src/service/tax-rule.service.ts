	async update(taxruleDTO: TaxruleDTO): Promise<Taxrule001mb> {
		const taxrule001mb = new Taxrule001mb();
		taxrule001mb.setProperties(taxruleDTO);
		await this.taxRuleRepository.update({ id: taxrule001mb.id }, taxrule001mb);
		return taxrule001mb;
	}

	async findAll(): Promise<Taxrule001mb[]> {
		return this.taxRuleRepository.find();
	}

	findOne(id: string): Promise<Taxrule001mb> {
		return this.taxRuleRepository.findOne(id);
	}

	async remove(id: string): Promise<void> {
		await this.taxRuleRepository.delete(id);
	}

	async downloadPdf(@Req() request: Request, @Res() response: Response) {
        let taxRule: any[] = await this.taxRuleRepository.find();


        var document = {
            html: html,
            data: {
                TaxRule: taxRule,
            },
            path: path.join(`./pdf/taxRule.pdf`),
            type: "",
        };
        var options = {
            format: "A3",
            orientation: "landscape",
            border: "10mm",
            header: {
                height: "45mm",
            },
            footer: {
                height: "28mm",

            }
        };
        if (taxRule.length < 0) {
            return;
        }
        else {
            pdf.create(document, options).then((pathRes) => {
                const filestream = createReadStream(pathRes.filename);
                response.writeHead(200, {
                    "Content-Disposition": "attachment;filename=" + "taxRule.pdf",
                    'Content-Type': 'application/pdf'
                });
                filestream.pipe(response);
            }).catch((error) => {
                console.error(error);
            });
        };

    }

