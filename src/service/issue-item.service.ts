    async update(issueditemDTO: IssueditemDTO): Promise<Issueditem001mb> {
        const issueditem001mb = new Issueditem001mb();
        issueditem001mb.setProperties(issueditemDTO);
        await this.issueitemRepository.update({ issueId: issueditem001mb.issueId }, issueditem001mb);
        return issueditem001mb;
    }

    async findAll(): Promise<Issueditem001mb[]> {
        return this.issueitemRepository.find();
    }

    findOne(id: string): Promise<Issueditem001mb> {
        return this.issueitemRepository.findOne(id);
    }

    async remove(id: string): Promise<void> {
        await this.issueitemRepository.delete(id);
    }

    async downloadPdf(@Req() request: Request, @Res() response: Response) {
        let issueItem: any[] = await this.issueitemRepository.find();


        var document = {
            html: html,
            data: {
                IssueItem: issueItem,
            },
            path: path.join(`./pdf/issueItem.pdf`),
            type: "",
        };
        var options = {
            format: "A3",
            orientation: "landscape",
            border: "10mm",
            header: {
                height: "45mm",
            },
            footer: {
                height: "28mm",

            }
        };
        if (issueItem.length < 0) {
            return;
        }
        else {
            pdf.create(document, options).then((pathRes) => {
                const filestream = createReadStream(pathRes.filename);
                response.writeHead(200, {
                    "Content-Disposition": "attachment;filename=" + "issueItem.pdf",
                    'Content-Type': 'application/pdf'
                });
                filestream.pipe(response);
            }).catch((error) => {
                console.error(error);
            });
        };

    }

