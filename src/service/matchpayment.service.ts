    async downloadPdf(@Req() request: Request, @Res() response: Response) {
        let matchpayment: any[] = await this.matchpaymentRepository.find();


        var document = {
            html: html,
            data: {
                Matchpayment: matchpayment,
            },
            path: path.join(`./pdf/matchpayment.pdf`),
            type: "",
        };
        var options = {
            format: "A3",
            orientation: "landscape",
            border: "10mm",
            header: {
                height: "45mm",
            },
            footer: {
                height: "28mm",

            }
        };
        if (matchpayment.length < 0) {
            return;
        }
        else {
            pdf.create(document, options).then((pathRes) => {
                const filestream = createReadStream(pathRes.filename);
                response.writeHead(200, {
                    "Content-Disposition": "attachment;filename=" + "matchpayment.pdf",
                    'Content-Type': 'application/pdf'
                });
                filestream.pipe(response);
            }).catch((error) => {
                console.error(error);
            });
        };

    }

